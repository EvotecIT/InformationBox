namespace InformationBox.Config;

/// <summary>
/// Logical grouping for navigation links.
/// </summary>
public enum LinkSection
{
    /// <summary>
    /// Links that direct users to support workflows.
    /// </summary>
    Support,
    /// <summary>
    /// Links that launch commonly used internal applications.
    /// </summary>
    Apps,
    /// <summary>
    /// Links that provide documentation or help content.
    /// </summary>
    Help,
    /// <summary>
    /// Links that belong to a custom grouping.
    /// </summary>
    Custom
}
